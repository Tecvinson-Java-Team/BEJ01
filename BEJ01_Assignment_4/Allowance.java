public class Allowance {
    // Fields for different types of allowances
    private double basic;
    private double transportation;
    private double wardrobe;
    private double education;
    private double feeding;

    // Getter method for basic allowance
    public double getBasic() {
        return basic;
    }

    // Setter method for basic allowance
    public void setBasic(double basic) {
        this.basic = basic;
    }

    // Getter method for transportation allowance
    public double getTransportation() {
        return transportation;
    }

    // Setter method for transportation allowance
    public void setTransportation(double transportation) {
        this.transportation = transportation;
    }

    // Getter method for wardrobe allowance
    public double getWardrobe() {
        return wardrobe;
    }

    // Setter method for wardrobe allowance
    public void setWardrobe(double wardrobe) {
        this.wardrobe = wardrobe;
    }

    // Getter method for education allowance
    public double getEducation() {
        return education;
    }

    // Setter method for education allowance
    public void setEducation(double education) {
        this.education = education;
    }

    // Getter method for feeding allowance
    public double getFeeding() {
        return feeding;
    }

    // Setter method for feeding allowance
    public void setFeeding(double feeding) {
        this.feeding = feeding;
    }
}
